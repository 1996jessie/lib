package reserve;

public class ReserveBean {
	private int rnum;
	private int rmnum;
	private int rbnum;
	private String rbcode;
	
	public int getRnum() {
		return rnum;
	}
	public void setRnum(int rnum) {
		this.rnum = rnum;
	}
	public int getRmnum() {
		return rmnum;
	}
	public void setRmnum(int rmnum) {
		this.rmnum = rmnum;
	}
	public int getRbnum() {
		return rbnum;
	}
	public void setRbnum(int rbnum) {
		this.rbnum = rbnum;
	}
	public String getRbcode() {
		return rbcode;
	}
	public void setRbcode(String rbcode) {
		this.rbcode = rbcode;
	}
	


	
}
